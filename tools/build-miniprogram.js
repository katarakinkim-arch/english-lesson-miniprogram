// tools/build-miniprogram.js — 脚手架：把全 9 科拆成「子包/学科」，主包只放轻量索引
// 用法：node build-miniprogram.js
const fs = require('fs');
const path = require('path');

const BASE = path.join(__dirname, '..');
const { SUBJECTS } = require('../utils/subjects.js');

function ensureDir(p) { fs.mkdirSync(p, { recursive: true }); }
function copyFile(from, to) { fs.copyFileSync(from, to); }
function writeFile(to, content) { fs.writeFileSync(to, content, 'utf8'); }

// ---------- 1. 构建轻量索引 data/index.js ----------
function buildIndex() {
  const entries = [];
  for (const s of SUBJECTS) {
    const L = require(path.join(BASE, 'data', s.dataFile));
    L.forEach((l) => {
      entries.push({
        id: l.id,
        title: l.title,
        subject: l.subject,
        book: l.book,
        unitNumber: l.unitNumber,
        unitTitle: l.unitTitle,
        lessonType: l.lessonType || '',
        lessonTypeName: l.lessonTypeName || '',
        duration: l.duration || 45,
        grade: l.grade || ''
      });
    });
  }
  const header = '// AUTO-GENERATED by tools/build-miniprogram.js — 轻量元数据索引（主包用：学科选择/收藏/记录解析）。请勿手改。\n';
  writeFile(path.join(BASE, 'data', 'index.js'), header + 'module.exports = ' + JSON.stringify(entries, null, 2) + ';\n');
  return entries.length;
}

