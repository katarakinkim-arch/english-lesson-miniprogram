// ---------- 3. 生成子包 ----------
function buildSubpackages() {
  const srcIndexWxss = path.join(BASE, 'pages', 'index', 'index.wxss');
  const srcDetailWxss = path.join(BASE, 'tools', 'templates', 'detail.wxss');
  const srcDetailWxml = path.join(BASE, 'tools', 'templates', 'detail.wxml');
  let detailWxml = fs.readFileSync(srcDetailWxml, 'utf8');

  for (const s of SUBJECTS) {
    const root = path.join(BASE, 'subpackages', s.root);
    ensureDir(path.join(root, 'data'));
    ensureDir(path.join(root, 'pages', 'list'));
    ensureDir(path.join(root, 'pages', 'detail'));

    // 数据复制进子包（统一命名 lessons.js）
    copyFile(path.join(BASE, 'data', s.dataFile), path.join(root, 'data', 'lessons.js'));

    // 计算该学科册列表（用于筛选 chips）
    const L = require(path.join(root, 'data', 'lessons.js'));
    const bookSet = [];
    L.forEach((l) => { if (bookSet.indexOf(l.book) === -1) bookSet.push(l.book); });
    const books = [{ value: '0', label: '全部', book: '__all__' }].concat(
      bookSet.map((b, i) => ({ value: String(i + 1), label: b, book: b }))
    );
    const meta = { subject: s.name, root: s.root, emoji: s.emoji, color: s.color, books: books };

    // list 页
    writeFile(path.join(root, 'pages', 'list', 'list.js'), LIST_JS.split('__META__').join(JSON.stringify(meta)));
    writeFile(path.join(root, 'pages', 'list', 'list.json'), JSON.stringify({ navigationBarTitleText: s.name + '教案', enablePullDownRefresh: true, usingComponents: {} }, null, 2));
    writeFile(path.join(root, 'pages', 'list', 'list.wxml'), LIST_WXML);
    copyFile(srcIndexWxss, path.join(root, 'pages', 'list', 'list.wxss'));

    // detail 页
    writeFile(path.join(root, 'pages', 'detail', 'detail.js'), DETAIL_JS);
    writeFile(path.join(root, 'pages', 'detail', 'detail.json'), JSON.stringify({ navigationBarTitleText: '教案详情', usingComponents: {} }, null, 2));
    writeFile(path.join(root, 'pages', 'detail', 'detail.wxml'), detailWxml);
    copyFile(srcDetailWxss, path.join(root, 'pages', 'detail', 'detail.wxss'));
  }
}

// ---------- 4. 清理旧的独立 detail 主包页（已迁移到子包） ----------
function cleanupOldDetail() {
  const old = path.join(BASE, 'pages', 'detail');
  if (fs.existsSync(old)) {
    fs.rmSync(old, { recursive: true, force: true });
    console.log('removed old pages/detail');
  }
}

const n = buildIndex();
buildSubpackages();
cleanupOldDetail();
console.log('build done. index entries =', n, '| subpackages =', SUBJECTS.length);
